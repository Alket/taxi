export function nextId(kind: "booking" | "driver" | "zone" | "rule" | "user") {
  switch (kind) {
    case "booking":
      return `bk_${bookingSeq++}`
    case "driver":
      return `drv_${String(driverSeq++).padStart(2, "0")}`
    case "zone":
      return `zone_${String(zoneSeq++).padStart(2, "0")}`
    case "rule":
      return `pr_${String(ruleSeq++).padStart(2, "0")}`
    case "user":
      return `usr_${String(userSeq++).padStart(2, "0")}`
  }
}

