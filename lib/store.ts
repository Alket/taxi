function iso(offsetMinutes: number): string {
  return new Date(Date.now() + offsetMinutes * 60_000).toISOString()
}

const ORDER: BookingStatus[] = [
  "pending",
  "confirmed",
  "driver_assigned",
  "driver_accepted",
  "en_route",
  "arrived",
  "in_progress",
  "completed",
]

function buildTimeline(status: BookingStatus, pickupOffsetMin: number) {
  if (status === "cancelled") {
    return [
      { status: "pending" as BookingStatus, timestamp: iso(-2880) },
      { status: "confirmed" as BookingStatus, timestamp: iso(-2820) },
      { status: "cancelled" as BookingStatus, timestamp: iso(-1440) },
    ]
  }
  const reached = ORDER.slice(0, ORDER.indexOf(status) + 1)
  return reached.map((s, i) => ({
    status: s,
    timestamp: iso(pickupOffsetMin - (reached.length - i) * 45),
  }))
}

